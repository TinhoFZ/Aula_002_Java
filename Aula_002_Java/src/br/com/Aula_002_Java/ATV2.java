package br.com.Aula_002_Java;

public class ATV2 {

	public static void main(String[] args) {
		
		
		int media1 = (8 + 9 + 7) / 3;
		int media2 = (4 + 5 + 6) / 3;
		
		int soma_medias = media1 + media2;
		int media_medias = soma_medias / 2;
		
		System.out.println("A media entre os números 8, 9 e 7 é igual a: " + media1);

		System.out.println("A media entre os números 4, 5 e 6 é igual a: " + media2);
		
		System.out.println("A soma entre as duas médias é igual a: " + soma_medias);
		
		System.out.println("A media entre as médias é igual a: " + media_medias);
		
	}

}
